alias Obscura.Tiktoken
alias Obscura.Tiktoken.Encoding

cases = [
  {"short", "Rachel works at OpenAI in Paris."},
  {"medium", String.duplicate("Please call Rachel at +1 415 555 0100. ", 40)},
  {"unicode", String.duplicate("我非常渴望与人工智能一起工作 ", 40)},
  {"repeated_digits", String.duplicate("0", 1_000)}
]

encodings = ["cl100k_base", "o200k_base", "o200k_harmony"]
iterations = 50
warmup_iterations = 5

results =
  for encoding_name <- encodings, {case_name, text} <- cases do
    encoding = Tiktoken.get_encoding!(encoding_name)
    for _iteration <- 1..warmup_iterations, do: Encoding.encode!(encoding, text)

    timings =
      for _iteration <- 1..iterations do
        {micros, tokens} = :timer.tc(fn -> Encoding.encode!(encoding, text) end)
        {micros / 1_000.0, length(tokens)}
      end

    latencies = Enum.map(timings, &elem(&1, 0))
    token_counts = Enum.map(timings, &elem(&1, 1))
    sorted = Enum.sort(latencies)
    mean = Enum.sum(latencies) / length(latencies)
    p95 = Enum.at(sorted, floor((length(sorted) - 1) * 0.95))
    tokens = Enum.max(token_counts)

    %{
      encoding: encoding_name,
      case: case_name,
      bytes: byte_size(text),
      tokens: tokens,
      iterations: iterations,
      mean_ms: Float.round(mean, 4),
      p95_ms: Float.round(p95, 4),
      tokens_per_second: Float.round(tokens / (mean / 1_000.0), 2)
    }
  end

output_dir = Path.expand("../reports", __DIR__)
File.mkdir_p!(output_dir)

json_path = Path.join(output_dir, "tiktoken_benchmark.json")
md_path = Path.join(output_dir, "tiktoken_benchmark.md")

File.write!(json_path, Jason.encode!(%{results: results}, pretty: true))

rows =
  Enum.map(results, fn result ->
    "| #{result.encoding} | #{result.case} | #{result.bytes} | #{result.tokens} | #{result.mean_ms} | #{result.p95_ms} | #{result.tokens_per_second} |"
  end)

markdown = [
  "# Obscura.Tiktoken Benchmark",
  "",
  "Pure Elixir tokenizer benchmark generated by `eval/tiktoken/benchmark_tiktoken.exs`.",
  "",
  "| Encoding | Case | Bytes | Tokens | Mean ms | P95 ms | Tokens/sec |",
  "| --- | --- | ---: | ---: | ---: | ---: | ---: |",
  rows,
  ""
]

File.write!(md_path, IO.iodata_to_binary(Enum.intersperse(List.flatten(markdown), "\n")))

IO.puts("wrote #{json_path}")
IO.puts("wrote #{md_path}")
