def write_predictions(path: Path, results: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for result in results:
            row = {
                "sample_id": result["sample"]["id"],
                "predictions": sanitize_examples(result["predicted"]),
                "latency_ms": result["latency_ms"],
            }
            handle.write(json.dumps(row, sort_keys=True) + "\n")


def write_json(path: Path, report: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(report, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def sanitize_examples(value: Any) -> Any:
    if isinstance(value, list):
        return [sanitize_examples(item) for item in value]
    if isinstance(value, tuple):
        return [sanitize_examples(item) for item in value]
    if isinstance(value, dict):
        sanitized = {}
        for key, item in value.items():
            sanitized[key] = "[omitted]" if key in {"value", "text"} else sanitize_examples(item)
        return sanitized
    return value


def char_to_byte(text: str, char_offset: int | None) -> int | None:
    if char_offset is None:
        return None
    return len(text[:char_offset].encode("utf-8"))


def byte_slice(text: str, byte_start: int, byte_end: int) -> str:
    return text.encode("utf-8")[byte_start:byte_end].decode("utf-8")


def build_run_id(dataset: str, model: str, scope: str, suffix: str) -> str:
    model_part = safe_name(model)
    base = f"presidio_python_{dataset}_{model_part}_{scope}"
    return f"{base}_{safe_name(suffix)}" if suffix else base


def scope(args: argparse.Namespace) -> str:
    base_scope = "full" if args.full else "smoke"
    if args.template_split == "all":
        return base_scope
    return f"{args.template_split}_{base_scope}"


def safe_name(value: str) -> str:
    safe = "".join(char.lower() if char.isalnum() else "_" for char in value)
    return "_".join(part for part in safe.split("_") if part)


def format_metric(value: float | None) -> str:
    return "n/a" if value is None else f"{value:.4f}"


def limitation_lines(limitations: list[str]) -> str:
    return "\n".join(f"- {item}" for item in limitations)


def git_sha() -> str:
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            check=True,
            capture_output=True,
            text=True,
        )
        return result.stdout.strip()
    except Exception:
        return "unknown"


