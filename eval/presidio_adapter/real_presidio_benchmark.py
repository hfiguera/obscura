def template_summary(samples: list[dict[str, Any]]) -> dict[str, Any]:
    template_ids = [sample.get("template_id") for sample in samples if sample.get("template_id") is not None]
    counts: dict[Any, int] = {}
    for template_id in template_ids:
        counts[template_id] = counts.get(template_id, 0) + 1
    return {
        "template_count": len(set(template_ids)),
        "sample_count_by_template": dict(sorted(counts.items())[:25]),
    }


