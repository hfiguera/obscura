@dataclass
class LoadedDataset:
    name: str
    path: Path
    version: str
    original_sample_count: int
    invalid_samples: list[dict[str, Any]]
    samples: list[dict[str, Any]]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run real Presidio Analyzer and write Obscura-compatible reports."
    )
    parser.add_argument("--dataset", default="generated_small", choices=sorted(DATASETS))
    parser.add_argument("--limit", type=int, default=5)
    parser.add_argument("--full", action="store_true")
    parser.add_argument("--language", default="en")
    parser.add_argument("--model", default="en_core_web_lg")
    parser.add_argument(
        "--selection",
        help="Fingerprint selection generated by mix obscura.presidio.prepare.",
    )
    parser.add_argument(
        "--lock",
        default="eval/presidio_adapter/requirements-authoritative.lock",
    )
    parser.add_argument("--warmup", type=int, default=1)
    parser.add_argument("--run-suffix", default="")
    parser.add_argument("--out-dir", default="eval/reports")
    parser.add_argument("--predictions-dir", default="eval/predictions")
    parser.add_argument(
        "--template-split",
        default="all",
        choices=["all", "template_train", "template_heldout"],
    )
    parser.add_argument("--template-train-ratio", type=float, default=0.7)
    parser.add_argument(
        "--sample-ids",
        help="Comma-separated explicit sample IDs. Defaults to Presidio-supported smoke subset.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    loaded = load_dataset(args.dataset)
    if AnalyzerEngine is None:
        raise SystemExit("missing optional dependency presidio_analyzer")
    analyzer = AnalyzerEngine()
    analyzer_entities = sorted(analyzer.get_supported_entities(language=args.language))
    selection = load_selection(args.selection, loaded) if args.selection else None

    if selection:
        mapping = selection["entity_policy"]["source_entity_mapping"]
        supported_source_entities = sorted(
            entity for entity in mapping if entity in analyzer_entities
        )
        supported_entities = selection["entity_policy"]["entities"]
        samples = selected_samples_from_selection(loaded.samples, selection)
        split = selection["dataset"]["template_split"]
    else:
        mapping = ENTITY_MAP
        supported_source_entities = analyzer_entities
        supported_entities = sorted(
            {
                ENTITY_MAP[source_entity]
                for source_entity in supported_source_entities
                if source_entity in ENTITY_MAP
            }
        )
        selected_samples, split = split_samples_by_template(
            loaded.samples, args.template_split, args.template_train_ratio
        )
        samples = select_samples(selected_samples, supported_entities, args)
    run_id = build_run_id(args.dataset, args.model, scope(args), args.run_suffix)
    predictions_path = (
        Path(args.predictions_dir)
        / f"{run_id}.jsonl"
    )

    warmup(analyzer, samples, supported_source_entities, args.language, args.warmup)
    results = run_presidio(
        analyzer, samples, supported_source_entities, args.language, mapping
    )
    write_predictions(predictions_path, results)

    metrics = score_results(results, supported_entities)
    report = build_report(
        run_id=run_id,
        args=args,
        loaded=loaded,
        samples=samples,
        split=split,
        supported_source_entities=supported_source_entities,
        supported_entities=supported_entities,
        metrics=metrics,
        predictions_path=predictions_path,
        selection=selection,
    )

    out_dir = Path(args.out_dir)
    json_path = out_dir / f"{run_id}.json"
    markdown_path = out_dir / f"{run_id}.md"
    write_json(json_path, report)
    markdown_path.write_text(markdown(report), encoding="utf-8")

    print(
        json.dumps(
            {
                "run_id": run_id,
                "samples": len(samples),
                "sample_ids": [sample["id"] for sample in samples],
                "json": str(json_path),
                "markdown": str(markdown_path),
                "predictions": str(predictions_path),
                "metrics": report["metrics"],
            },
            sort_keys=True,
        )
    )
    return 0


def load_dataset(name: str) -> LoadedDataset:
    metadata = DATASETS[name]
    path = Path(metadata["path"])
    raw = json.loads(path.read_text(encoding="utf-8"))
    raw_samples = raw if isinstance(raw, list) else raw.get("samples", raw.get("data", []))

    samples: list[dict[str, Any]] = []
    invalid_samples: list[dict[str, Any]] = []
    for index, raw_sample in enumerate(raw_samples):
        sample = normalize_sample(raw_sample, index)
        reason = validate_sample(sample)
        if reason:
            invalid_samples.append({"id": sample["id"], "reason": reason})
        else:
            samples.append(sample)

    return LoadedDataset(
        name=name,
        path=path,
        version=metadata["version"],
        original_sample_count=len(raw_samples),
        invalid_samples=invalid_samples,
        samples=samples,
    )


def load_selection(path_value: str, loaded: LoadedDataset) -> dict[str, Any]:
    path = Path(path_value)
    selection = json.loads(path.read_text(encoding="utf-8"))
    dataset = selection.get("dataset", {})
    protocol_path = Path(selection["protocol_path"])

    checks = [
        (sha256_file(loaded.path), dataset.get("sha256"), "dataset fingerprint"),
        (
            sha256_file(protocol_path),
            selection.get("protocol_sha256"),
            "protocol fingerprint",
        ),
        (
            sha256_json(dataset.get("ordered_sample_ids")),
            dataset.get("sample_ids_sha256"),
            "sample ID fingerprint",
        ),
        (
            sha256_json(
                {
                    "entities": selection["entity_policy"]["entities"],
                    "source_entity_mapping": selection["entity_policy"][
                        "source_entity_mapping"
                    ],
                }
            ),
            selection["entity_policy"].get("sha256"),
            "entity policy fingerprint",
        ),
        (
            sha256_json(
                {
                    key: value
                    for key, value in selection["scoring"].items()
                    if key != "sha256"
                }
            ),
            selection["scoring"].get("sha256"),
            "scoring fingerprint",
        ),
    ]

    for actual, expected, label in checks:
        if actual != expected:
            raise SystemExit(f"{label} mismatch: expected {expected}, got {actual}")

    if dataset.get("name") != loaded.name:
        raise SystemExit("selection dataset name does not match --dataset")

    ids = dataset.get("ordered_sample_ids", [])
    if len(ids) != len(set(map(str, ids))):
        raise SystemExit("selection contains duplicate sample IDs")

    return selection


def selected_samples_from_selection(
    samples: list[dict[str, Any]], selection: dict[str, Any]
) -> list[dict[str, Any]]:
    by_id = {str(sample["id"]): sample for sample in samples}
    expected_ids = selection["dataset"]["ordered_sample_ids"]
    selected = []

    for sample_id in expected_ids:
        sample = by_id.get(str(sample_id))
        if sample is None:
            raise SystemExit(f"selection sample ID missing from dataset: {sample_id}")
        selected.append(sample)

    if [sample["id"] for sample in selected] != expected_ids:
        raise SystemExit("selected sample order differs from protocol selection")
    return selected


def normalize_sample(raw_sample: dict[str, Any], index: int) -> dict[str, Any]:
    text = first_present(raw_sample, ["full_text", "text", "sentence", "content"], "")
    spans = []
    for raw_span in first_present(raw_sample, ["spans", "entities", "tags"], []):
        source_entity = first_present(raw_span, ["entity_type", "entity", "label"], None)
        value = first_present(raw_span, ["entity_value", "value", "text"], None)
        char_start = first_present(raw_span, ["start_position", "start", "char_start"], None)
        char_end = first_present(raw_span, ["end_position", "end", "char_end"], None)
        spans.append(
            {
                "entity": ENTITY_MAP.get(source_entity, "unknown"),
                "byte_start": char_to_byte(text, char_start),
                "byte_end": char_to_byte(text, char_end),
                "char_start": char_start,
                "char_end": char_end,
                "source_entity": source_entity,
                "value": value,
                "metadata": {},
            }
        )

    return {
        "id": first_present(raw_sample, ["id", "sample_id"], index),
        "text": text,
        "template_id": first_present(raw_sample, ["template_id"], None),
        "spans": spans,
    }


def first_present(values: dict[str, Any], keys: list[str], default: Any) -> Any:
    for key in keys:
        if key in values:
            return values[key]
    return default


def validate_sample(sample: dict[str, Any]) -> str | None:
    text = sample["text"]
    for span in sample["spans"]:
        start = span["byte_start"]
        end = span["byte_end"]
        if start is None or end is None:
            return "missing_offsets"
        if start < 0 or end < 0 or start >= end or end > len(text.encode("utf-8")):
            return f"invalid_byte_range:{start}:{end}"
        if span.get("value") is not None and byte_slice(text, start, end) != span["value"]:
            return "value_mismatch"
    return None


def select_samples(
    samples: list[dict[str, Any]], supported_entities: list[str], args: argparse.Namespace
) -> list[dict[str, Any]]:
    if args.sample_ids:
        wanted = {parse_sample_id(sample_id) for sample_id in args.sample_ids.split(",")}
        return [sample for sample in samples if sample["id"] in wanted or str(sample["id"]) in wanted]

    if args.full:
        return samples

    return [
        sample
        for sample in sorted(samples, key=sample_sort_key)
        if any(span["entity"] in supported_entities for span in sample["spans"])
    ][: args.limit]


def split_samples_by_template(
    samples: list[dict[str, Any]], template_split: str, train_ratio: float
) -> tuple[list[dict[str, Any]], dict[str, Any]]:
    template_ids = sorted({sample["template_id"] for sample in samples if sample.get("template_id") is not None})
    if template_split == "all":
        return samples, {
            "name": "all",
            "strategy": "all",
            "train_ratio": None,
            "template_count": len(template_ids),
            "selected_template_count": len(template_ids),
            "heldout_template_count": 0,
            "selected_template_ids": template_ids,
            "train_template_ids": [],
            "heldout_template_ids": [],
        }

    if train_ratio <= 0.0 or train_ratio >= 1.0:
        raise SystemExit("--template-train-ratio must be greater than 0.0 and less than 1.0")

    train_count = int(len(template_ids) * train_ratio)
    train_count = max(1, min(train_count, max(len(template_ids) - 1, 1)))
    train_template_ids = template_ids[:train_count]
    heldout_template_ids = template_ids[train_count:]
    selected_template_ids = train_template_ids if template_split == "template_train" else heldout_template_ids
    selected_template_set = set(selected_template_ids)
    selected_samples = [sample for sample in samples if sample.get("template_id") in selected_template_set]
    return selected_samples, {
        "name": template_split,
        "strategy": "template_id",
        "train_ratio": train_ratio,
        "template_count": len(template_ids),
        "selected_template_count": len(selected_template_ids),
        "heldout_template_count": len(heldout_template_ids),
        "selected_template_ids": selected_template_ids,
        "train_template_ids": train_template_ids,
        "heldout_template_ids": heldout_template_ids,
    }


def sample_sort_key(sample: dict[str, Any]) -> tuple[int, Any]:
    sample_id = sample["id"]
    if isinstance(sample_id, int):
        return (0, sample_id)
    return (1, str(sample_id))


def parse_sample_id(value: str) -> int | str:
    value = value.strip()
    try:
        return int(value)
    except ValueError:
        return value


def run_presidio(
    analyzer: AnalyzerEngine,
    samples: list[dict[str, Any]],
    supported_source_entities: list[str],
    language: str,
    entity_map: dict[str, str],
) -> list[dict[str, Any]]:
    results = []
    for sample in samples:
        start = time.perf_counter()
        analyzer_results = analyzer.analyze(
            text=sample["text"],
            language=language,
            entities=supported_source_entities,
            return_decision_process=False,
        )
        latency_ms = (time.perf_counter() - start) * 1000
        predictions = [
            normalize_prediction(sample["text"], result, entity_map)
            for result in sorted(analyzer_results, key=lambda result: (result.start, result.end))
            if result.entity_type in entity_map
        ]
        results.append(
            {
                "sample": sample,
                "expected": sample["spans"],
                "predicted": predictions,
                "latency_ms": latency_ms,
            }
        )
    return results


def normalize_prediction(
    text: str, result: Any, entity_map: dict[str, str]
) -> dict[str, Any]:
    return {
        "entity": entity_map[result.entity_type],
        "byte_start": char_to_byte(text, result.start),
        "byte_end": char_to_byte(text, result.end),
        "char_start": result.start,
        "char_end": result.end,
        "source_entity": result.entity_type,
        "score": result.score,
        "value": text[result.start : result.end],
        "metadata": {},
    }


def score_results(results: list[dict[str, Any]], supported_entities: list[str]) -> dict[str, Any]:
    scores = [score(result["expected"], result["predicted"], supported_entities) for result in results]
    counts = empty_counts(total_samples=len(results))
    latency = [result["latency_ms"] for result in results]

    for item in scores:
        for key in counts:
            if key != "total_samples":
                counts[key] += item[key]

    output = {
        **counts,
        **ratios(counts),
        "latency": latency_summary(latency),
        "per_entity": aggregate_per_entity(scores),
        "examples": aggregate_examples(scores),
    }
    return output


def score(
    expected: list[dict[str, Any]], predicted: list[dict[str, Any]], supported_entities: list[str]
) -> dict[str, Any]:
    supported_expected = [span for span in expected if span["entity"] in supported_entities]
    unsupported_expected = [span for span in expected if span["entity"] not in supported_entities]

    true_positives, remaining_expected, remaining_predicted = match_exact(
        supported_expected, predicted
    )
    offset_mismatches, remaining_expected, remaining_predicted = match_offset_mismatch(
        remaining_expected, remaining_predicted
    )
    wrong_entity_type, remaining_expected, remaining_predicted = match_wrong_entity(
        remaining_expected, remaining_predicted
    )

    false_negatives = remaining_expected
    false_positives = remaining_predicted
    counts = {
        "true_positives": len(true_positives),
        "false_positives": len(false_positives),
        "false_negatives": len(false_negatives),
        "offset_mismatches": len(offset_mismatches),
        "wrong_entity_type": len(wrong_entity_type),
        "unsupported_expected_spans": len(unsupported_expected),
        "total_expected_spans": len(expected),
        "total_supported_expected_spans": len(supported_expected),
        "total_predicted_spans": len(predicted),
        "total_samples": 1,
    }
    return {
        **counts,
        **ratios(counts),
        "per_entity": per_entity_metrics(supported_expected, predicted),
        "examples": {
            "false_positives": sanitize_examples(false_positives),
            "false_negatives": sanitize_examples(false_negatives),
            "offset_mismatches": sanitize_examples(offset_mismatches),
            "wrong_entity_type": sanitize_examples(wrong_entity_type),
            "unsupported": sanitize_examples(unsupported_expected),
        },
    }


def match_exact(
    expected: list[dict[str, Any]], predicted: list[dict[str, Any]]
) -> tuple[list[Any], list[dict[str, Any]], list[dict[str, Any]]]:
    return match_by(expected, predicted, exact_match)


def match_offset_mismatch(
    expected: list[dict[str, Any]], predicted: list[dict[str, Any]]
) -> tuple[list[Any], list[dict[str, Any]], list[dict[str, Any]]]:
    return match_by(expected, predicted, offset_mismatch)


def match_wrong_entity(
    expected: list[dict[str, Any]], predicted: list[dict[str, Any]]
) -> tuple[list[Any], list[dict[str, Any]], list[dict[str, Any]]]:
    return match_by(expected, predicted, wrong_entity_match)


def match_by(expected: list[dict[str, Any]], predicted: list[dict[str, Any]], predicate: Any):
    matched = []
    remaining_expected = []
    remaining_predicted = list(predicted)
    for expected_span in expected:
        index = next(
            (
                idx
                for idx, predicted_span in enumerate(remaining_predicted)
                if predicate(expected_span, predicted_span)
            ),
            None,
        )
        if index is None:
            remaining_expected.append(expected_span)
        else:
            prediction = remaining_predicted.pop(index)
            matched.append((expected_span, prediction))
    return matched, remaining_expected, remaining_predicted


def exact_match(expected: dict[str, Any], predicted: dict[str, Any]) -> bool:
    return (
        expected["entity"] == predicted["entity"]
        and expected["byte_start"] == predicted["byte_start"]
        and expected["byte_end"] == predicted["byte_end"]
    )


def offset_mismatch(expected: dict[str, Any], predicted: dict[str, Any]) -> bool:
    return expected["entity"] == predicted["entity"] and iou(expected, predicted) > 0 and not exact_match(
        expected, predicted
    )


def wrong_entity_match(expected: dict[str, Any], predicted: dict[str, Any]) -> bool:
    return expected["entity"] != predicted["entity"] and iou(expected, predicted) > 0


def iou(left: dict[str, Any], right: dict[str, Any]) -> float:
    intersection = max(0, min(left["byte_end"], right["byte_end"]) - max(left["byte_start"], right["byte_start"]))
    union = max(left["byte_end"], right["byte_end"]) - min(left["byte_start"], right["byte_start"])
    return 0.0 if union == 0 else intersection / union


def per_entity_metrics(
    expected: list[dict[str, Any]], predicted: list[dict[str, Any]]
) -> dict[str, Any]:
    entities = sorted({span["entity"] for span in expected + predicted})
    return {
        entity: score_entity(
            [span for span in expected if span["entity"] == entity],
            [span for span in predicted if span["entity"] == entity],
        )
        for entity in entities
    }


def score_entity(expected: list[dict[str, Any]], predicted: list[dict[str, Any]]) -> dict[str, Any]:
    true_positives, remaining_expected, remaining_predicted = match_exact(expected, predicted)
    offset_mismatches, remaining_expected, remaining_predicted = match_offset_mismatch(
        remaining_expected, remaining_predicted
    )
    counts = {
        "support_count": len(expected),
        "prediction_count": len(predicted),
        "true_positives": len(true_positives),
        "false_positives": len(remaining_predicted),
        "false_negatives": len(remaining_expected),
        "offset_mismatches": len(offset_mismatches),
        "wrong_entity_type": 0,
    }
    return {**counts, **ratios(counts)}


def aggregate_per_entity(scores: list[dict[str, Any]]) -> dict[str, Any]:
    grouped: dict[str, list[dict[str, Any]]] = {}
    for score_item in scores:
        for entity, metrics in score_item["per_entity"].items():
            grouped.setdefault(entity, []).append(metrics)

    aggregated = {}
    for entity, metrics_list in grouped.items():
        counts = {
            "support_count": 0,
            "prediction_count": 0,
            "true_positives": 0,
            "false_positives": 0,
            "false_negatives": 0,
            "offset_mismatches": 0,
            "wrong_entity_type": 0,
        }
        for metrics in metrics_list:
            for key in counts:
                counts[key] += metrics[key]
        aggregated[entity] = {**counts, **ratios(counts)}
    return aggregated


def aggregate_examples(scores: list[dict[str, Any]]) -> dict[str, Any]:
    keys = ["false_positives", "false_negatives", "offset_mismatches", "wrong_entity_type", "unsupported"]
    return {
        key: [item for score_item in scores for item in score_item["examples"][key]][:10]
        for key in keys
    }


def ratios(counts: dict[str, Any]) -> dict[str, float | None]:
    tp = counts.get("true_positives", 0)
    fp = counts.get("false_positives", 0)
    fn = counts.get("false_negatives", 0)
    precision = divide(tp, tp + fp)
    recall = divide(tp, tp + fn)
    return {
        "precision": precision,
        "recall": recall,
        "f1": f_score(precision, recall, 1),
        "f2": f_score(precision, recall, 2),
    }


def divide(numerator: float, denominator: float) -> float | None:
    return None if denominator == 0 else numerator / denominator


def f_score(precision: float | None, recall: float | None, beta: float) -> float | None:
    if precision is None or recall is None:
        return None
    if precision == 0 and recall == 0:
        return 0.0
    beta_squared = beta * beta
    denominator = beta_squared * precision + recall
    return 0.0 if denominator == 0 else (1 + beta_squared) * precision * recall / denominator


def latency_summary(values: list[float]) -> dict[str, float]:
    if not values:
        return {"mean_ms": 0.0, "p50_ms": 0.0, "p95_ms": 0.0, "max_ms": 0.0}
    sorted_values = sorted(values)
    return {
        "mean_ms": sum(values) / len(values),
        "p50_ms": percentile(sorted_values, 0.50),
        "p95_ms": percentile(sorted_values, 0.95),
        "max_ms": sorted_values[-1],
    }


def percentile(sorted_values: list[float], value: float) -> float:
    index = int(((len(sorted_values) - 1) * value + 0.9999999999))
    return sorted_values[index]


